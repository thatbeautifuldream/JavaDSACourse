package IntroTopics;

public class HelloWorld {
    public static void main(String[] args) {
        System.out.print("Getting Started with Java.\n"); // "\n" is a escape sequence that illustrates a new line
        System.out.printf("Java is %d percent fun!\n", 100); // the printf stands for print with formatting
        System.out.println("Hello World!"); // Standard print with new line at end from java
    }
}
